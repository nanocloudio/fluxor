// Shared 5×7 bitmap font — single source of truth for raster text on the
// content plane. `include!`'d by BOTH the host reference
// `tools/src/content_render.rs` (std) and the on-device `content_controls` PIC
// module (no_std), so the two can never drift. Pure: only arrays, slices, and a
// `match` — no std, no alloc, no float.
//
// Each glyph is 7 rows; each row's low 5 bits are columns (bit 4 = leftmost).
// Covered set: space, 0-9, A-Z, and `.:-/>` — enough for action legends and
// short labels; unknown chars render blank.

pub const GLYPH_W: u16 = 5;
pub const GLYPH_H: u16 = 7;

/// 5×7 glyph rows for a character, or `None` if unsupported.
pub fn glyph(c: char) -> Option<[u8; 7]> {
    let g: [u8; 7] = match c.to_ascii_uppercase() {
        ' ' => [0, 0, 0, 0, 0, 0, 0],
        '0' => [0x0E, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0E],
        '1' => [0x04, 0x0C, 0x04, 0x04, 0x04, 0x04, 0x0E],
        '2' => [0x0E, 0x11, 0x01, 0x02, 0x04, 0x08, 0x1F],
        '3' => [0x1F, 0x02, 0x04, 0x02, 0x01, 0x11, 0x0E],
        '4' => [0x02, 0x06, 0x0A, 0x12, 0x1F, 0x02, 0x02],
        '5' => [0x1F, 0x10, 0x1E, 0x01, 0x01, 0x11, 0x0E],
        '6' => [0x06, 0x08, 0x10, 0x1E, 0x11, 0x11, 0x0E],
        '7' => [0x1F, 0x01, 0x02, 0x04, 0x08, 0x08, 0x08],
        '8' => [0x0E, 0x11, 0x11, 0x0E, 0x11, 0x11, 0x0E],
        '9' => [0x0E, 0x11, 0x11, 0x0F, 0x01, 0x02, 0x0C],
        'A' => [0x0E, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11],
        'B' => [0x1E, 0x11, 0x11, 0x1E, 0x11, 0x11, 0x1E],
        'C' => [0x0E, 0x11, 0x10, 0x10, 0x10, 0x11, 0x0E],
        'D' => [0x1C, 0x12, 0x11, 0x11, 0x11, 0x12, 0x1C],
        'E' => [0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x1F],
        'F' => [0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x10],
        'G' => [0x0E, 0x11, 0x10, 0x17, 0x11, 0x11, 0x0F],
        'H' => [0x11, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11],
        'I' => [0x0E, 0x04, 0x04, 0x04, 0x04, 0x04, 0x0E],
        'J' => [0x07, 0x02, 0x02, 0x02, 0x02, 0x12, 0x0C],
        'K' => [0x11, 0x12, 0x14, 0x18, 0x14, 0x12, 0x11],
        'L' => [0x10, 0x10, 0x10, 0x10, 0x10, 0x10, 0x1F],
        'M' => [0x11, 0x1B, 0x15, 0x15, 0x11, 0x11, 0x11],
        'N' => [0x11, 0x19, 0x15, 0x13, 0x11, 0x11, 0x11],
        'O' => [0x0E, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E],
        'P' => [0x1E, 0x11, 0x11, 0x1E, 0x10, 0x10, 0x10],
        'Q' => [0x0E, 0x11, 0x11, 0x11, 0x15, 0x12, 0x0D],
        'R' => [0x1E, 0x11, 0x11, 0x1E, 0x14, 0x12, 0x11],
        'S' => [0x0F, 0x10, 0x10, 0x0E, 0x01, 0x01, 0x1E],
        'T' => [0x1F, 0x04, 0x04, 0x04, 0x04, 0x04, 0x04],
        'U' => [0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E],
        'V' => [0x11, 0x11, 0x11, 0x11, 0x11, 0x0A, 0x04],
        'W' => [0x11, 0x11, 0x11, 0x15, 0x15, 0x1B, 0x11],
        'X' => [0x11, 0x11, 0x0A, 0x04, 0x0A, 0x11, 0x11],
        'Y' => [0x11, 0x11, 0x0A, 0x04, 0x04, 0x04, 0x04],
        'Z' => [0x1F, 0x01, 0x02, 0x04, 0x08, 0x10, 0x1F],
        '.' => [0x00, 0x00, 0x00, 0x00, 0x00, 0x0C, 0x0C],
        ':' => [0x00, 0x0C, 0x0C, 0x00, 0x0C, 0x0C, 0x00],
        '-' => [0x00, 0x00, 0x00, 0x1F, 0x00, 0x00, 0x00],
        '/' => [0x01, 0x01, 0x02, 0x04, 0x08, 0x10, 0x10],
        '>' => [0x08, 0x04, 0x02, 0x01, 0x02, 0x04, 0x08],
        _ => return None,
    };
    Some(g)
}

/// Bounds-checked pixel write (named to avoid colliding with an includer's
/// own `put`).
#[inline]
fn glyph_put(fb: &mut [u16], stride: u16, x: u16, y: u16, c: u16) {
    let idx = (y as usize) * (stride as usize) + (x as usize);
    if idx < fb.len() {
        fb[idx] = c;
    }
}

/// Draw one 5×7 glyph at `(x, y)` scaled by `scale` (≥1). Clipped by the buffer.
pub fn draw_glyph(fb: &mut [u16], stride: u16, x: u16, y: u16, c: char, scale: u16, color: u16) {
    let Some(rows) = glyph(c) else { return };
    let s = scale.max(1);
    for (ry, row) in rows.iter().enumerate() {
        for cx in 0..GLYPH_W {
            if row & (1 << (GLYPH_W - 1 - cx)) != 0 {
                for dy in 0..s {
                    for dx in 0..s {
                        glyph_put(fb, stride, x + cx * s + dx, y + (ry as u16) * s + dy, color);
                    }
                }
            }
        }
    }
}

/// Draw a left-aligned string at `(x, y)`, scaled, with 1-column glyph spacing.
/// Returns the x advance (pixels consumed). Stops at the buffer's stride width.
pub fn draw_text(
    fb: &mut [u16],
    stride: u16,
    x: u16,
    y: u16,
    text: &str,
    scale: u16,
    color: u16,
) -> u16 {
    let s = scale.max(1);
    let advance = (GLYPH_W + 1) * s;
    let mut cx = x;
    for c in text.chars() {
        if cx >= stride {
            break;
        }
        draw_glyph(fb, stride, cx, y, c, s, color);
        cx += advance;
    }
    cx.saturating_sub(x)
}

/// Draw ASCII `bytes` as text (each byte → its char). Avoids `str::from_utf8`,
/// which the PIC runtime does not link — use this to render a `[u8]` name buffer.
/// Bytes outside the covered glyph set render blank. Returns the x advance.
pub fn draw_bytes(
    fb: &mut [u16],
    stride: u16,
    x: u16,
    y: u16,
    bytes: &[u8],
    scale: u16,
    color: u16,
) -> u16 {
    let s = scale.max(1);
    let advance = (GLYPH_W + 1) * s;
    let mut cx = x;
    for &b in bytes {
        if cx >= stride {
            break;
        }
        draw_glyph(fb, stride, cx, y, b as char, s, color);
        cx += advance;
    }
    cx.saturating_sub(x)
}
