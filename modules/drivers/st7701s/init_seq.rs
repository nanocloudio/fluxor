//! ST7701S register initialization sequence for 480×480 RGB mode.
//!
//! Ported from Waveshare RP2350-Touch-LCD-4 BSP (bsp_st7701.c).
//! Each entry is (command_byte, data_bytes[]).
//! After the sequence, SLEEP_OUT (0x11) and DISPLAY_ON (0x29) each need 120ms delay.
//!
//! NOTE: Uses fixed-size arrays (no embedded pointers) because PIC modules
//! execute from XIP flash — &'static [u8] slice pointers in .rodata
//! cannot be relocated, so they'd point to wrong addresses.

/// Max data bytes per command in this init sequence.
pub const MAX_DATA: usize = 16;

/// Single init command: register address + data payload (no pointers).
pub struct InitCmd {
    pub cmd: u8,
    pub len: u8,
    pub data: [u8; MAX_DATA],
}

/// Helper to build an InitCmd from a variable-length slice at compile time.
const fn cmd(reg: u8, d: &[u8]) -> InitCmd {
    let mut data = [0u8; MAX_DATA];
    let mut i = 0;
    while i < d.len() {
        data[i] = d[i];
        i += 1;
    }
    InitCmd { cmd: reg, len: d.len() as u8, data }
}

/// Number of commands in the init sequence.
pub const INIT_SEQ_LEN: usize = 34;

/// Complete ST7701S init sequence for 480×480 RGB parallel mode.
/// 9-bit SPI: bit[8]=0 for command, bit[8]=1 for data.
pub static INIT_SEQ: [InitCmd; INIT_SEQ_LEN] = [
    // CMD2 Bank 0: gamma, line setting, porch, inversion
    cmd(0xFF, &[0x77, 0x01, 0x00, 0x00, 0x10]),
    cmd(0xC0, &[0x3B, 0x00]),   // LNESET: (59+1)*8 = 480 lines
    cmd(0xC1, &[0x0D, 0x02]),   // PORCTRL: VBP=13, VFP=2
    cmd(0xC2, &[0x31, 0x05]),   // INVSEL
    cmd(0xCD, &[0x08]),          // COLCTRL: EPF=1

    // Positive gamma
    cmd(0xB0, &[0x00, 0x11, 0x18, 0x0E, 0x11, 0x06, 0x07, 0x08,
                0x07, 0x22, 0x04, 0x12, 0x0F, 0xAA, 0x31, 0x18]),
    // Negative gamma
    cmd(0xB1, &[0x00, 0x11, 0x19, 0x0E, 0x12, 0x07, 0x08, 0x08,
                0x08, 0x22, 0x04, 0x11, 0x11, 0xA9, 0x32, 0x18]),

    // CMD2 Bank 1: power, voltage
    cmd(0xFF, &[0x77, 0x01, 0x00, 0x00, 0x11]),
    cmd(0xB0, &[0x60]),   // VRHS
    cmd(0xB1, &[0x32]),   // VCOMS
    cmd(0xB2, &[0x07]),   // VGHSS
    cmd(0xB3, &[0x80]),   // TESTCMD
    cmd(0xB5, &[0x49]),   // VGLS
    cmd(0xB7, &[0x85]),   // PWCTLR1
    cmd(0xB8, &[0x21]),   // PWCTLR2
    cmd(0xC1, &[0x78]),   // SPD1
    cmd(0xC2, &[0x78]),   // SPD2

    // GIP timing
    cmd(0xE0, &[0x00, 0x1B, 0x02]),
    cmd(0xE1, &[0x08, 0xA0, 0x00, 0x00, 0x07, 0xA0, 0x00, 0x00,
                0x00, 0x44, 0x44]),
    cmd(0xE2, &[0x11, 0x11, 0x44, 0x44, 0xED, 0xA0, 0x00, 0x00,
                0xEC, 0xA0, 0x00, 0x00]),
    cmd(0xE3, &[0x00, 0x00, 0x11, 0x11]),
    cmd(0xE4, &[0x44, 0x44]),
    cmd(0xE5, &[0x0A, 0xE9, 0xD8, 0xA0, 0x0C, 0xEB, 0xD8, 0xA0,
                0x0E, 0xED, 0xD8, 0xA0, 0x10, 0xEF, 0xD8, 0xA0]),
    cmd(0xE6, &[0x00, 0x00, 0x11, 0x11]),
    cmd(0xE7, &[0x44, 0x44]),
    cmd(0xE8, &[0x09, 0xE8, 0xD8, 0xA0, 0x0B, 0xEA, 0xD8, 0xA0,
                0x0D, 0xEC, 0xD8, 0xA0, 0x0F, 0xEE, 0xD8, 0xA0]),
    cmd(0xEB, &[0x02, 0x00, 0xE4, 0xE4, 0x88, 0x00, 0x40]),
    cmd(0xEC, &[0x3C, 0x00]),
    cmd(0xED, &[0xAB, 0x89, 0x76, 0x54, 0x02, 0xFF, 0xFF, 0xFF,
                0xFF, 0xFF, 0xFF, 0x20, 0x45, 0x67, 0x98, 0xBA]),

    // CMD2 Bank 3
    cmd(0xFF, &[0x77, 0x01, 0x00, 0x00, 0x13]),
    cmd(0xE5, &[0xE4]),

    // Return to user commands (CMD1)
    cmd(0xFF, &[0x77, 0x01, 0x00, 0x00, 0x00]),
    cmd(0x21, &[]),              // INVON
    cmd(0x3A, &[0x60]),          // COLMOD: RGB666 (18-bit, 16-pin bus with EPF=1)
];

/// SLEEP_OUT command (requires 120ms delay after)
pub const CMD_SLEEP_OUT: u8 = 0x11;

/// DISPLAY_ON command (requires 120ms delay after)
pub const CMD_DISPLAY_ON: u8 = 0x29;
